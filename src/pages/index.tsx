import Head from "next/head";
import { useEffect, useState } from "react";
import { useRouter } from "next/router";

export default function Home() {
  const router = useRouter();
  const [ready, setReady] = useState(false);

  useEffect(() => {
    if (
      localStorage.getItem("access_token") &&
      localStorage.getItem("refresh_token") &&
      localStorage.getItem("expires_at")
    ) {
      router.push("/loggedin").then(() => setReady(true));
    } else {
      setReady(true);
    }
  }, []);

  interface RedirectURL {
    url: string;
  }

  function login() {
    fetch("/api/spotify/verify")
      .then((res) => {
        console.log(res);
        return res.json();
      })
      .then((json: RedirectURL) => {
        console.log(json);
        window.location.assign(json.url);
      });
  }

  return (
    <>
      {!ready ? (
        <></>
      ) : (
        <>
          <Head>
            <title>
              CaDance | A fine tuned running and listening experience
            </title>
            <meta name="description" content="Generated by create next app" />
            <meta
              name="viewport"
              content="width=device-width, initial-scale=1"
            />
            <link rel="icon" href="/favicon.ico" />
          </Head>
          <div className="outer">
            <div className="inner">
              <h1 className="header">CADANCE</h1>
              <p className="app-description">
                A fine tuned running and listening experience
              </p>
              <div className="log-in-buttons">
                <button className="spotify-button" onClick={login}>
                  Login to Spotify
                </button>
              </div>
              <section className="info-section">
                <div className="info-box">
                  <div className="info-box-icon">
                    <img src="img/music-note-white.png" alt="" />
                  </div>
                  <p>Select the desired genre and duration of your Cadance.</p>
                </div>
                <div className="info-box">
                  <div className="info-box-icon">
                    <img src="img/running-shoe-white.png" alt="" />
                  </div>
                  <p>
                    Cadance creates a playlist based on your preferences and
                    running cadence and queues songs accordingly.
                  </p>
                </div>
                <div className="info-box">
                  <div className="info-box-icon">
                    <img src="img/save-icon-white.png" alt="" />
                  </div>
                  <p>Save your Cadances for future reuse.</p>
                </div>
              </section>
            </div>
          </div>
        </>
      )}
    </>
  );
}
